package OOPS.abstraction;

abstract class abstract_class {
abstract void display(); // Abstract method with no body
}
//What is an abstract class?
//An abs
// An abstract class is a class that cannot be instantiated on its own and is meant to be subclassed. It can contain abstract methods (without implementation) and concrete methods (with implementation). Abstract classes are used to define a common interface for subclasses while allowing them to provide specific implementations for the abstract methods.